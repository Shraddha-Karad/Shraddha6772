package Strings;

public class CharAtDemo {

	public static void main(String args[]) {
		
		String name = "Shraddha";
		
		char ch = name.charAt(2);
		
		System.out.println("String is : "+name);
		System.out.println("Char at position 2 : "+ch);
		System.out.println("Char at position 4 : "+name.charAt(4));
		System.out.println("Char at position 6 : "+name.charAt(6));
		
	}
}
